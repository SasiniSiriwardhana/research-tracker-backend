package lk.ijse.cmjd.research_tracker.enums;

/**
 * Enumeration of project lifecycle statuses.
 *
 * <ul>
 *   <li>PLANNING   - Project is in the planning phase.</li>
 *   <li>ACTIVE     - Project is currently active and ongoing.</li>
 *   <li>ON_HOLD    - Project has been temporarily paused.</li>
 *   <li>COMPLETED  - Project has been successfully completed.</li>
 *   <li>ARCHIVED   - Project has been archived for reference.</li>
 * </ul>
 */
public enum Status {
    PLANNING,
    ACTIVE,
    ON_HOLD,
    COMPLETED,
    ARCHIVED
}
